/// Shared design tokens so cards, buttons, and sheets across the app agree
/// on how "rounded" and how "spaced" things are, instead of each widget
/// picking its own number.
class AppRadius {
  const AppRadius._();

  static const sm = 12.0;
  static const md = 16.0;
  static const lg = 20.0;
  static const xl = 24.0;
  static const pill = 999.0;
}

class AppSpacing {
  const AppSpacing._();

  static const xs = 6.0;
  static const sm = 10.0;
  static const md = 16.0;
  static const lg = 20.0;
  static const xl = 28.0;
}
